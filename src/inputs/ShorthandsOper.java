package inputs;

public class ShorthandsOper {
  
	public static void main(String[] args) {
		int guests = 3;
		guests = guests + 4;
		System.out.println(guests);
	    guests += 5; 
	    //guests = guests + 5; is the same way
	    guests += 2;
	    System.out.println(guests);
	    guests -= 4;
	    System.out.println(guests);
	    
	    int apples = 3;
	    apples = apples * 2;
	    System.out.println(apples);
	    apples *= 4;
	    System.out.println(apples);
	    
	    int bananas = 4;
	    bananas -=2;
	    System.out.println(bananas);
	    bananas += 6;
	    System.out.println(bananas);
	    
	    
	    
	}
	
	
	
	
	
	
}
