package forloops;

public class ForLoopAndOddNumbers {

	public static void main(String[] args) {
      for(int a = 0; a <= 50; a++) {
    	  if(a % 2 == 1) {
    		  System.out.println(a);
    	  }else {
    		  System.out.println(a + " even number");
    	  }
    	  
		
		
      }

     
      
      
      
		
	}

}
